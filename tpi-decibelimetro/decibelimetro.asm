INICIO:
    CFG_DISP
    CFG_TECLADO
    CFG_UART_TX
    CFG_ADC
    CFG_ALARM
    BANKSEL INTCON
    BSF INTCON, PEIE  ; Habilita interrupciones perifericas
    BSF INTCON, GIE
LOOP:
    CALL    Delay_50us ; Delay de adquisicion (20us aprox.)
    BANKSEL ADCON0
    BSF     ADCON0, GO_DONE ; Iniciar conversion  
    CALL REFRESH_DISPLAYS
    GOTO LOOP

ISR_INICIO:
    MOVWF W_TEMP
    SWAPF STATUS, W
    MOVWF STATUS_TEMP
    BTFSC INTCON, RBIF
    GOTO ISR_TECL
    BTFSC PIR1, ADIF
    GOTO  ISR_ADC
    GOTO ISR_FIN
	    
ISR_FIN:
    SWAPF STATUS_TEMP, W
    MOVWF STATUS
    SWAPF W_TEMP, F
    SWAPF W_TEMP, W
    RETFIE 

ISR_ADC:
    BANKSEL ADRESH
    MOVFW   ADRESH         ; Solo 8 bits
    MOVWF   ResultadoH	
    CALL    CONVERT_TO_DB
    CALL    AD_BCD	   ; Convierte a binario
    CALL    TX_ADQ_ASCII
    CALL    CHECK_ALARM
    BCF	    PIR1,ADIF	   ; Bajo bandera
    GOTO    ISR_FIN

ISR_TECL:
    CALL TECL_PRESS
    CALL TECL_LOAD
    CLRF PORTB
    BCF INTCON, RBIF
    GOTO ISR_FIN
    
TECL_PRESS:
    CLRF NTECL
    MOVLW B'01110000'
    MOVWF PORTB
    CALL DELAY_20MS
    GOTO TEST_COL
	    
TEST_COL:
    INCF NTECL, F
    BTFSS PORTB, RB3
    GOTO TECL_DELAY
    INCF NTECL, F
    BTFSS PORTB, RB2
    GOTO TECL_DELAY
    INCF NTECL, F
    BTFSS PORTB, RB1
    GOTO TECL_DELAY
    GOTO TEST_FIL
	    
TEST_FIL:    
    BTFSS PORTB, RB4
    GOTO TECL_RST ;si ya probe todo termino la interrupcion
    BSF STATUS, C ;sino recorro la siguiente fila
    RRF PORTB
    GOTO TEST_COL ;(columna por columna)
    RETURN
	    
TECL_RST:    
    CLRF NTECL
    CLRF PORTB
    BCF INTCON, RBIF
    RETURN
	    
TECL_DELAY:  
    CALL DELAY_20MS
    BTFSS PORTB, RB3 ;loops antirrebote por software
    GOTO $-1
    BTFSS PORTB, RB2
    GOTO $-1
    BTFSS PORTB, RB1
    GOTO $-1
    BTFSS PORTB, RB0
    GOTO $-1
    BCF INTCON, RBIF ;bajo bandera de interrupcion
    CLRF PORTB ;limpio puerto
    RETURN
	    
TECL_LOAD:
    MOVFW NTECL
    CALL CONVERT_NTECL
    MOVWF NTECL
    MOVLW NTECL_ERR
    SUBWF NTECL, W
    BTFSC STATUS,Z
    RETURN
    MOVLW DISP_VAL
    SUBWF DISP_STATE, W
    BTFSC STATUS,Z
    GOTO FROM_DISP_VAL
    MOVLW DISP_UMB
    SUBWF DISP_STATE, W
    BTFSC STATUS,Z
    GOTO FROM_DISP_UMB
    MOVLW DISP_TCL1
    SUBWF DISP_STATE, W
    BTFSC STATUS,Z
    GOTO FROM_DISP_TCL1
    MOVLW DISP_TCL2
    SUBWF DISP_STATE, W
    BTFSC STATUS,Z
    GOTO FROM_DISP_TCL2
    RETURN

FROM_DISP_VAL:
    MOVLW NTECL_BACK
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO CLEAR_ALARM
    MOVLW NTECL_SET
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO LOAD_DISP_UMB
    RETURN
    
FROM_DISP_UMB:
    MOVLW NTECL_BACK
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO LOAD_DISP_VAL
    MOVLW NTECL_SET
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO LOAD_DISP_VAL
    GOTO LOAD_TCL1
    
FROM_DISP_TCL1:
    MOVLW NTECL_SET
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO CONFIRM_TCL
    MOVLW NTECL_BACK
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO LOAD_DISP_VAL
    GOTO LOAD_TCL2
    
FROM_DISP_TCL2:
    MOVLW NTECL_SET
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO CONFIRM_TCL
    MOVLW NTECL_BACK
    SUBWF NTECL, W
    BTFSC STATUS, Z
    GOTO LOAD_DISP_VAL
    RETURN

CLEAR_ALARM:
    BCF PORTC, RC0
    RETURN

LOAD_DISP_VAL:
    MOVFW DECENAS
    MOVWF DISP4
    MOVFW UNIDAD
    MOVWF DISP3
    MOVLW DISP_D_LITERAL
    MOVWF DISP2
    MOVLW DISP_B_LITERAL
    MOVWF DISP1
    MOVLW DISP_VAL
    MOVWF DISP_STATE
    CLRF TEMP_UMBRAL_BCD
    RETURN    

LOAD_DISP_UMB:
    SWAPF UMBRAL_BCD, W
    ANDLW 0x0F
    MOVWF DISP4
    MOVFW UMBRAL_BCD
    ANDLW 0x0F
    MOVWF DISP3
    MOVLW DISP_D_LITERAL
    MOVWF DISP2
    MOVLW DISP_B_LITERAL
    MOVWF DISP1
    MOVLW DISP_UMB
    MOVWF DISP_STATE
    RETURN
    
LOAD_TCL1:
    MOVFW NTECL
    MOVWF TEMP_UMBRAL_BCD
    SWAPF TEMP_UMBRAL_BCD, W
    ANDLW 0x0F
    MOVWF DISP4
    MOVFW TEMP_UMBRAL_BCD
    ANDLW 0x0F
    MOVWF DISP3
    MOVLW DISP_D_LITERAL
    MOVWF DISP2
    MOVLW DISP_B_LITERAL
    MOVWF DISP1
    MOVLW DISP_TCL1
    MOVWF DISP_STATE
    RETURN
    
LOAD_TCL2:
    SWAPF TEMP_UMBRAL_BCD, W
    IORWF NTECL, W
    MOVWF TEMP_UMBRAL_BCD
    SWAPF TEMP_UMBRAL_BCD, W
    ANDLW 0x0F
    MOVWF DISP4
    MOVFW TEMP_UMBRAL_BCD
    ANDLW 0x0F
    MOVWF DISP3
    MOVLW DISP_D_LITERAL
    MOVWF DISP2
    MOVLW DISP_B_LITERAL
    MOVWF DISP1
    MOVLW DISP_TCL2
    MOVWF DISP_STATE
    RETURN
    
CONFIRM_TCL:
    MOVFW TEMP_UMBRAL_BCD
    MOVWF UMBRAL_BCD
    GOTO LOAD_DISP_VAL

AD_BCD:	    CLRF    CENTENA
	    CLRF    DECENAS
	    CLRF    UNIDAD
TEST_CENTENA MOVLW  d'100' 
	    SUBWF   ResultadoH,0   ;ResultadoH - 100 -> W
	    BTFSC   STATUS,C
	    GOTO    ADD_CENTENA	    ;si ResultadoH>100 sumo CENTENA    
TEST_DECENA MOVLW   d'10' 
	    SUBWF   ResultadoH,0    ;ResultadoH - 10 -> W
	    BTFSC   STATUS,C
	    GOTO    ADD_DECENA	    ;si ADRESH>10 sumo DECENA
	    MOVFW   ResultadoH
	    MOVWF   UNIDAD	    ;sino ADRESH va a UNIDAD
	    RETURN    
ADD_CENTENA BCF	    STATUS,C
	    MOVWF   ResultadoH	    ;guardo el resto
	    INCF    CENTENA	    ;cuento una centena
	    GOTO    TEST_CENTENA    ;sigo buscando centenas
ADD_DECENA  BCF	    STATUS,C
	    MOVWF   ResultadoH	    ;guardo el resto
	    INCF    DECENAS	    ;cuento una decena
	    GOTO    TEST_DECENA	    ;sigo buscando decenas

REFRESH_DISPLAYS:
    MOVLW DISP_VAL
    SUBWF DISP_STATE, W
    BTFSC STATUS, Z
    CALL LOAD_LAST_ADQ
    MOVLW .4
    MOVWF COUNTER_DISPLAY
LOOP_DISPLAY:
    MOVLW .4
    SUBWF COUNTER_DISPLAY, W
    BTFSC STATUS, Z
    CALL LOAD_DISPLAY4
    MOVLW .3
    SUBWF COUNTER_DISPLAY, W
    BTFSC STATUS, Z
    CALL LOAD_DISPLAY3
    MOVLW .2
    SUBWF COUNTER_DISPLAY, W
    BTFSC STATUS, Z
    CALL LOAD_DISPLAY2
    MOVLW .1
    SUBWF COUNTER_DISPLAY, W
    BTFSC STATUS, Z
    CALL LOAD_DISPLAY1
    DECFSZ COUNTER_DISPLAY, F
    GOTO LOOP_DISPLAY
    RETURN
    
LOAD_DISPLAY4:
    MOVF COUNTER_DISPLAY, W
    CALL TABLE_ON_DISPLAY_AC
    MOVWF TEMP_ON_DISP_MASK
    MOVFW PORTC
    ANDLW CLEAR_RC4_RC1_MASK
    IORWF TEMP_ON_DISP_MASK, W
    MOVWF PORTC
    MOVFW DISP4
    CALL TABLE_DECO_DISPLAY_AC
    MOVWF PORTD
    CALL DELAY_5MS
    RETURN
    
LOAD_DISPLAY3:	
    MOVF COUNTER_DISPLAY, W
    CALL TABLE_ON_DISPLAY_AC
    MOVWF TEMP_ON_DISP_MASK
    MOVFW PORTC
    ANDLW CLEAR_RC4_RC1_MASK
    IORWF TEMP_ON_DISP_MASK, W
    MOVWF PORTC
    MOVFW DISP3
    CALL TABLE_DECO_DISPLAY_AC
    MOVWF PORTD
    CALL DELAY_5MS
    RETURN
    
LOAD_DISPLAY2:	
    MOVF COUNTER_DISPLAY, W
    CALL TABLE_ON_DISPLAY_AC
    MOVWF TEMP_ON_DISP_MASK
    MOVFW PORTC
    ANDLW CLEAR_RC4_RC1_MASK
    IORWF TEMP_ON_DISP_MASK, W
    MOVWF PORTC
    MOVFW DISP2
    CALL TABLE_DECO_DISPLAY_AC
    MOVWF PORTD
    CALL DELAY_5MS
    RETURN
    
LOAD_DISPLAY1:	
    MOVF COUNTER_DISPLAY, W
    CALL TABLE_ON_DISPLAY_AC
    MOVWF TEMP_ON_DISP_MASK
    MOVFW PORTC
    ANDLW CLEAR_RC4_RC1_MASK
    IORWF TEMP_ON_DISP_MASK, W
    MOVWF PORTC
    MOVFW DISP1
    CALL TABLE_DECO_DISPLAY_AC
    MOVWF PORTD
    CALL DELAY_5MS
    RETURN
    
LOAD_LAST_ADQ:
    MOVFW DECENAS_DB
    MOVWF DISP4
    MOVFW UNIDAD_DB
    MOVWF DISP3
    RETURN
    
Delay_50us:
    BANKSEL ContadorDelay
    movlw   d'12'
    movwf   ContadorDelay
D1:
    NOP
    NOP
    DECFSZ  ContadorDelay, F
    GOTO    D1
    RETURN 

DELAY_5MS:	
    MOVLW   D'65'
    MOVWF   DELAY1
LOOPA:		
    MOVLW   D'25'
    MOVWF   DELAY2
LOOPB:		
    DECFSZ  DELAY2,F
    GOTO    LOOPB
    DECFSZ  DELAY1,F
    GOTO    LOOPA
    RETURN
		
DELAY_20MS:	
    MOVLW   D'255'
    MOVWF   DELAY3
LOOP1:		
    MOVLW   D'26'
    MOVWF   DELAY4
LOOP2:		
    DECFSZ  DELAY4,F
    GOTO    LOOP2
    DECFSZ  DELAY3,F
    GOTO    LOOP1
    RETURN	

UART_TX:
    BANKSEL TXSTA
    BTFSS   TXSTA, TRMT
    GOTO $-1
    BANKSEL TXREG
    MOVWF TXREG
    RETURN
    

TX_ADQ_ASCII:
    MOVFW CENTENA
    ADDLW .48
    CALL UART_TX
    MOVFW DECENAS
    ADDLW .48
    CALL UART_TX
    MOVFW UNIDAD
    ADDLW .48
    CALL UART_TX
    MOVLW .10 ; LF
    CALL UART_TX
    MOVLW .13 ; CR
    CALL UART_TX
    RETURN
    
CONVERT_TO_DB:
    BANKSEL PCLATH
    MOVFW PCLATH
    MOVWF TEMP_PCLATH
    MOVLW 0x02
    MOVWF PCLATH
    MOVFW ResultadoH
    ADDWF PCL, F
    
    MOVWF DECIBELES_BCD
    MOVFW DECIBELES_BCD
    ANDLW 0x0F
    MOVWF UNIDAD_DB
    MOVFW DECIBELES_BCD
    MOVWF DECENAS_DB
    SWAPF DECENAS_DB, W
    ANDLW 0x0F
    MOVWF DECENAS_DB   

    BANKSEL PCLATH
    MOVFW TEMP_PCLATH
    MOVWF PCLATH
    RETURN
    
CHECK_ALARM:
    MOVFW DECENAS_DB
    MOVWF DB_BCD
    SWAPF DB_BCD, F
    MOVFW UNIDAD_DB
    IORWF DB_BCD, F
    MOVFW DB_BCD
    SUBWF UMBRAL_BCD, W
    BTFSS STATUS, C
    BSF PORTC, RC0
    RETURN		

