CFG_UART_TX	MACRO
		BANKSEL PORTC
		BCF	PORTC, RC6
		BANKSEL TRISC
		BCF	TRISC, TRISC6 ; RC6 DIGITAL OUT
		BANKSEL RCSTA
		BSF	RCSTA, SPEN ; RC6 TX MODE
		BANKSEL TXSTA
		BSF	TXSTA, TXEN ; TX EN
		BCF	TXSTA, SYNC ; ASYNC
		BSF	TXSTA, BRGH ; HIGH SPEED
		BANKSEL BAUDCTL
		BCF	BAUDCTL, BRG16 ; 8 BIT
		BANKSEL SPBRG
		MOVLW	.25
		MOVWF	SPBRG ; 9600 Bd
		ENDM

CFG_ADC		MACRO
		BANKSEL TRISE
		MOVLW   b'00000001'
		MOVWF   TRISE
		BANKSEL ANSEL
		MOVLW   b'00100000'        ; AN5 anal√≥gico, resto digital
		MOVWF   ANSEL
		CLRF    ANSELH             ; Puertos digitales superiores
		BANKSEL ADCON1
		BCF     ADCON1, ADFM       ; 0 = justificado a la izquierda
		BANKSEL ADCON0
		MOVLW   b'10010101'        ; ADCS1=1 ADCS0=0 CHS=0101 (AN5 = RE0) ADON=1
		MOVWF   ADCON0
		BANKSEL	PIE1
		BSF	PIE1, ADIE		;Habilita int por ADC (fin de conversion)
		ENDM
		
CFG_ALARM	MACRO
		MOVLW 0x99
		MOVWF UMBRAL_BCD
        BANKSEL TRISC
        BCF TRISC, TRISC0  ; RC0 como salida
		BANKSEL PORTC
		BCF PORTC,RC0
		ENDM

CFG_DISP	MACRO
		BANKSEL TRISD		; (PORTD es digital por default)
		CLRF	TRISD		; PORTD Salida
		BANKSEL PORTD
		CLRF	PORTD		; PORTD estado bajo
		BANKSEL TRISC
		BCF TRISC, TRISC4
		BCF TRISC, TRISC3
		BCF TRISC, TRISC2
		BCF TRISC, TRISC1
		BANKSEL PORTC
		MOVFW PORTC
		ANDLW CLEAR_RC4_RC1_MASK
		IORLW B'00001110' ; rc3, rc2, rc1 en 1 rc4 on
		MOVWF PORTC
		BCF STATUS, RP0
		BCF STATUS, RP1
		MOVLW DISP_VAL
		MOVWF DISP_STATE
		MOVLW DISP_OFF
		MOVWF DISP4
		MOVWF DISP3
		MOVLW DISP_D_LITERAL
		MOVWF DISP2
		MOVLW DISP_B_LITERAL
		MOVWF DISP1
		ENDM

CFG_TECLADO	MACRO
		BANKSEL ANSELH		
		CLRF	ANSELH ; PORTB digital
		BANKSEL OPTION_REG
		BCF OPTION_REG, NOT_RBPU ;Habilito resistencias internas de pull-up
		MOVLW B'00001111'	
		MOVWF TRISB ; B<7,4>Salidas, B<3,0>Entradas
		MOVWF IOCB ;interrumpe B<3,0> 
		MOVWF WPUB ;pull-ups en B<3,0> 
		BANKSEL PORTB
		MOVLW B'11110000' ; PORTB estado bajo
		MOVWF PORTB
		BSF INTCON, RBIE ; Habilito interrupciones externas por PORTB
		CLRF NTECL
		ENDM

    ORG 0X00
    GOTO INICIO
    ORG 0X04
    GOTO ISR_INICIO
    ORG 0X05

